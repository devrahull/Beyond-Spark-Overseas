						<h2>Express Entry Steps !</h2>
							
							<br>

<strong>Step1:- Educational Credentials Assessment (ECA)</strong><br>
Once you apply for Express Entry Program. You need to evaluate your education from
Canadian designated organizations.<br>
<br>
<strong>Step2 - Preparation for IELTS</strong><br>
The candidate has to prepare for IELTS. This is an important step to file for Express Entry.
The candidate has to get good scores in IELTS.<br>
<br>
<strong>Step3- Calculate CRS ( Comprehensive Ranking System)</strong><br>
The calculation for CRS will be done on the below basis:

<br>
<br>
<ul class="job">
                            <li><strong>Age :</strong> Limits for migrating to Canada is between 18 to 45</li>
                            <li><strong>Language Proficiency:</strong>  IELTS Score Card</li>
                            <li><strong>Education:</strong>  ECA report</li>
                            <li><strong>Work Experience:</strong> Candidate should be skilled and have good work experience</li>
                            <li><strong>Adaptability:</strong>  If the Candidate having a sibling settled in Canada they get extra
points</li>
                            <li><strong>Arranges employment:</strong> If the candidate has arranged employment in Canada, they
get extra points.</li>
                                                    
                            </ul>
                          
<br>

<p><strong>Step4:-Create Profile</strong><br>
Once the Candidate gets the IELTS and ECA report then the Candidate has to submit EOI
in Express Entry Pool by Creating a profile.<br>
<br>
<strong>Step5 - ITA</strong><br>
After submitting the candidate profile in the express entry, the Candidate has to wait for
the draw. Once the ITA or PNP is obtained, the Candidate can apply for a PR visa within
60 days.<br>
<br>
<strong>Step6 - PR Approval</strong><br>
The candidate has to submit true and genuine information or documents in the PR
application. After reviewing your all documents Candidate gets the PR Approval.</p>
